function somar(a, b) {
    return a + b;
}

function subtrair(a, b) {
    return a - b;
}

function multiplicar(a, b) {
    return a*b;
}

function dividir(a, b) {
    return a/b;
}

function exponenciar(a, b) { // exponenciar(2, 3)
    return a**b; // resultado: 8
}

function modulo(a, b) {
    return a%b;
}